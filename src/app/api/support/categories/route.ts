// GET /api/support/categories
export async function GET() {
    const categories = await db.articleCategory.findMany({
        where: { isActive: true },
        orderBy: { sortOrder: 'asc' },
    })

    // Count published articles per category slug (plain string match)
    const counts = await db.supportArticle.groupBy({
        by: ['category'],
        where: { status: 'published' },
        _count: { id: true },
    })
    const countMap: Record<string, number> = {}
    for (const c of counts) { countMap[c.category] = c._count.id }

    const shaped = categories.map((cat: Record<string, unknown>) => ({
        ...cat,
        iconSvg: CATEGORY_ICONS[cat.slug as string] ?? '',
        _count: { articles: countMap[cat.slug as string] ?? 0 },
    }))

    return NextResponse.json(shaped)
}
